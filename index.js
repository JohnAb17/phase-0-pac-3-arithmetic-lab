function add(a, b) {
    return a + b;
}
function subtract(a ,b) {
    return a - b;
}
function multiply (a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}
function increment(n) {
    let value = ++n;
    return (value);
}
function decrement(n) {
    let value = --n;
    return (value);
}
function makeInt(n) {
    return parseInt(n, 10);
    
}
function preserveDecimal(n) {
    return parseFloat(n);
}